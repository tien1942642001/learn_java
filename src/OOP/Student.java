package OOP;

public class Student {
    public String name;
    public String email;
    public int age;
    public String gender;
    public String phoneNumber;
   public Student() {}

    public Student(String name, String email, int age, String gender, String phoneNumber) {
        this.name = name;
        this.email = email;
        this.age = age;
        this.gender = gender;
        this.phoneNumber = phoneNumber;
    }
}
